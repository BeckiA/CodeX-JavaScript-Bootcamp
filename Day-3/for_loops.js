// Working On For Loops
// 1. Displaying Hello World Five Times
for (let i = 1; i <= 5; i++) {
    console.log("Hello World", i);
    
}

// 2. Displaying Odd Numbers between 1 and 5:
for (let i = 1; i <= 5; i++){
    if (i % 2 !== 0) {
        console.log(i);
    }
}

// 3. Displaying Odd Numbers in Reverse Order:
for( let i = 5; i >= 1; i-- ){
    if (i % 2 !== 0) {
        console.log(i);
    }
}
